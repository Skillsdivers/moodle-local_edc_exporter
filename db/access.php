// Plugin capabilities. All of them apply inside a course, so they depend on the
// role the user has in that specific course.
$capabilities = [
    'local/edc_exporter:view' => [
        // Allows users to view the credential list and visual credential card.
        'riskbitmask' => RISK_PERSONAL,
        'captype' => 'read',
        'contextlevel' => CONTEXT_COURSE,
        'archetypes' => [
            'manager' => CAP_ALLOW,
            'coursecreator' => CAP_ALLOW,
            'editingteacher' => CAP_ALLOW,
            'teacher' => CAP_ALLOW,
        ],
    ],

    'local/edc_exporter:download' => [
        // Allows users to download the official JSON. It may contain learner personal data.
        'riskbitmask' => RISK_PERSONAL,
        'captype' => 'read',
        'contextlevel' => CONTEXT_COURSE,
        'archetypes' => [
            'manager' => CAP_ALLOW,
        ],
    ],

    'local/edc_exporter:downloadinternal' => [
        // More restricted capability: the internal JSON includes technical and tracking data.
        'riskbitmask' => RISK_PERSONAL,
        'captype' => 'read',
        'contextlevel' => CONTEXT_COURSE,
        'archetypes' => [
            'manager' => CAP_ALLOW,
        ],
    ],

    'local/edc_exporter:regenerate' => [
        // Allows users to retry a credential and change its JSON, status, and paths.
        'riskbitmask' => RISK_PERSONAL,
        'captype' => 'write',
        'contextlevel' => CONTEXT_COURSE,
        'archetypes' => [
            'manager' => CAP_ALLOW,
        ],
    ],

    'local/edc_exporter:revoke' => [
        // Allows users to invalidate an issued credential and store a revocation reason.
        'riskbitmask' => RISK_PERSONAL,
        'captype' => 'write',
        'contextlevel' => CONTEXT_COURSE,
        'archetypes' => [
            'manager' => CAP_ALLOW,
        ],
    ],

    'local/edc_exporter:generatepending' => [
        // Allows users to bulk-generate pending credentials for completed learners.
        'riskbitmask' => RISK_PERSONAL,
        'captype' => 'write',
        'contextlevel' => CONTEXT_COURSE,
        'archetypes' => [
            'manager' => CAP_ALLOW,
        ],
    ],
];
